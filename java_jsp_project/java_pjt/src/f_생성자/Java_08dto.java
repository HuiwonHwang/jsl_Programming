package f_생성자;

public class Java_08dto {
	private String id,name;
	private int ave;
	
	
	public Java_08dto(String id, String name, int ave) {
		super();
		this.id = id;
		this.name = name;
		this.ave = ave;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAve() {
		return ave;
	}
	public void setAve(int ave) {
		this.ave = ave;
	}
	
	
	
	
	
	
	
	
	
}
