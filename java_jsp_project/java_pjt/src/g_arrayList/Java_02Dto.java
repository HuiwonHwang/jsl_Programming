package g_arrayList;

public class Java_02Dto {
	String id, name, depart;
	int age;
	
	public Java_02Dto(String id, String name, String depart, int age) {
		this.id = id;
		this.name = name;
		this.depart = depart;
		this.age = age;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDepart() {
		return depart;
	}

	public void setDepart(String depart) {
		this.depart = depart;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	
	
}




